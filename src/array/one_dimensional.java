package array;
public class one_dimensional {
    public static void main(String[] args) {
         int a[] = new int[5];
        a[0]=20;
        a[1]=3;
        a[2]=90;
        a[3]=39;
        a[4]=49;
        for(int i=0;i<=5;i++){
            System.out.println(a[i]);
        }
        int b[]={110,30,34,5,5,7,9,9};
        for(int i=0;i<b.length;i++){
            System.out.println(b[i]);
        }
    }
    
}
